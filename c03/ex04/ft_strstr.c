char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	if (*to_find == '\0')
		return (str);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
			{
				return (str + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}

#include <stdio.h>

int	main()
{
	char str[] = "asdasd dsadsa Furkan Turedi";
	char to_find[] = "Furkan";
	printf("%s", ft_strstr(str, to_find));
}