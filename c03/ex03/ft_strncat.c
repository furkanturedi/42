char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (src[j] && j < nb)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}


#include <stdio.h>

int	main()
{
	char src[] = "Furkan";
	char dest[] = "Turedi ";
	
	printf("%s", ft_strncat(dest, src, 3));
}